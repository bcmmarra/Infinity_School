'''
Atividade 2 – Tipos de Dados
Crie variáveis com:
Nome (str)
Idade (int)
Altura (float)
Maioridade (bool)

Use type() para exibir o tipo de cada variável.
'''

nome = "Bruno"
idade = 35
altura = 1.82
maioridade = idade >= 18

print(f"A variável nome recebeu o valor: {nome} e possui o tipo {type(nome)}")
print(f"A variável idade recebeu o valor: {idade} e possui o tipo {type(idade)}")
print(f"A variável altura recebeu o valor: {altura} e possui o tipo {type(altura)}")
print(f"A variável maioridade recebeu o valor: {maioridade} e possui o tipo {type(maioridade)}")