'''
Categoria 3: Operações e Cálculo
🧪 Atividade 10 – Área de um retângulo
Peça a base e a altura de um retângulo ao usuário e calcule a área.

📌 Fórmula: área = base * altura
'''

base = int(input("Digite a base do retângulo (cm): "))
altura = int(input("Digite a altura do retângulo (cm): "))

area = base * altura

print(f"A área do retângulo é: {area}cm²")