# Listas
# As listas são coleções ordenadas e mutáveis (podem ser alteradas após a criação). Elas são perfeitas para guardar itens que podem mudar de ordem ou serem modificados.

# Sintaxe: Usam colchetes [] e os itens são separados por vírgulas.

# Exemplo:
lista_de_frutas = ['maçã', 'banana', 'cereja']


# Tuplas
# As tuplas são coleções ordenadas e imutáveis (não podem ser alteradas). Elas são usadas para guardar dados que não devem mudar, como as coordenadas de um ponto ou as notas de uma prova.

# Sintaxe: Usam parênteses () e os itens são separados por vírgulas.

# Exemplo:
coordenadas = (10, 20)

# Observação: Os parênteses são opcionais na maioria das vezes, mas é uma boa prática usá-los para maior clareza. Uma tupla de um único item precisa de uma vírgula no final, por exemplo: (10,).


# Sets (Conjuntos)
# Os sets são coleções não ordenadas e mutáveis que não permitem itens duplicados. Eles são ótimos para remover itens duplicados de uma coleção ou para operações matemáticas de conjunto (como união e intersecção).

# Sintaxe: Usam chaves {} e os itens são separados por vírgulas.

# Exemplo:
cores = {'vermelho', 'verde', 'azul'}

# Observação: Um set vazio é criado com set(), pois {} cria um dicionário vazio.

# Dicionários
# Os dicionários são coleções não ordenadas de pares de chave:valor. Eles são ideais para guardar dados de forma organizada, onde você pode acessar um valor rapidamente usando a sua chave.

# Sintaxe: Usam chaves {} e cada item é um par de chave e valor, separados por dois pontos :. Os pares são separados por vírgulas.

# Exemplo:
aluno = {'nome': 'João', 'idade': 25, 'cidade': 'São Paulo'}


# Observação: sets e dicionários usam chaves {}, a sintaxe para criar um dicionário sempre tem pares de chave e valor, enquanto um set tem apenas os valores.

# Resumo das Sintaxes
# Tipo de Dados	Sintaxe	Características
# Lista	        [ ]	Ordenada, mutável, permite duplicatas
# Tupla	        ( )	Ordenada, imutável, permite duplicatas
# Set	        { }	Não ordenada, mutável, não permite duplicatas
# Dicionário	{ }	Não ordenada, mutável, armazena pares chave:valor
