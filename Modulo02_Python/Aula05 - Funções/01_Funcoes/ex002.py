# Crie uma função chamada detalhes_produto que recebe três parâmetros: nome, preco e quantidade.

def detalhes_produto (nome, preco, quantidade):
    print(f'Produto: {nome} | Preço: R$ {preco} | Quantidade: {quantidade}')

detalhes_produto("Notebook", 3500, 2)
detalhes_produto("Smartphone", 1500, 12)
detalhes_produto("Relógio", 500, 10)
